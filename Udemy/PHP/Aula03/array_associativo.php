<?php
    // Array associativo é um array onde indentificamos e apontamos os valores para cada chave
    // Fazemos o apontamento utilizando a sequencia '=>'
    // Ex.:
    $arr = ["nome"=>"Philip", "idade"=>18];

    // É possivel imprimir o valor de cada chave passando o nome da chave como index
    echo $arr['nome']."\n";
    echo $arr['idade']."\n";
?>